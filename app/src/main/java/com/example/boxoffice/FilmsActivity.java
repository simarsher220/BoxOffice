    @Override
    public void onMovieItemClick(Film item) {
        Intent i = new Intent(FilmsActivity.this, DetailsActivity.class);
        i.putExtra(Intent.EXTRA_TEXT, item);
        i.putExtra("location", location);
        startActivity(i);
    }

    @Override
    public boolean onMenuItemClick(MenuItem menuItem) {
        location = (String) menuItem.getTitle();
        Uri uri = Uri.parse(API_URL).buildUpon().build();
        URL url = null;
        try {
            url = new URL(uri.toString());
        } catch (MalformedURLException e) {
            e.printStackTrace();
        }
        new FilmAsyncTask(this).execute(url);
        return true;
    }
